import { useState, useEffect } from "react";

const useFetchData = (url) => {
  const [data, setData] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  useEffect(() => {
    fetch(url)
      .then((response) => response.json())
      .then((data) => {
        setData(data);
        setLoading(false);
      })
      .catch((error) => {
        setError(error);
        setLoading(false);
      });
  }, [url]);

  return { data, loading, error };
};

export const useFetchCategories = () => useFetchData("https://test-ecommerce-gamma.vercel.app/categories");

export const useFetchProducts = () => useFetchData("https://test-ecommerce-gamma.vercel.app/products");